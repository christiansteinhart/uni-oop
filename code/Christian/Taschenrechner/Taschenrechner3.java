public class Taschenrechner3
{
   public float mul(float wert1, float wert2) {
        return wert1 * wert2;
    }

    public float div(float wert1, float wert2) {
        return wert1 / wert2;
    }

    public float sub(float wert1, float wert2) {
        return wert1 - wert2;
    }

    public float add(float wert1, float wert2) {
        return wert1 + wert2;
    }
}